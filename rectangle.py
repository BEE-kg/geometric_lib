def area(a, b): 
    ''' Функция для вычисления площади прямоугольника по длине и ширине
        Возвращает произведение введных сторон: a и b '''
    return a * b 
    
def perimeter(a, b): 
    ''' Функция для вычисления периметра прямоугольника по длине и ширине
        Возвращает сумму двух введенных сторон: a и b'''
    return a + b 
    
