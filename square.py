def area(a):
    '''Функция для вычисления площади квадрата по длине стороны
        Возвращает произведение введенной стороны а
        При вводе 4, получаем 16'''
    return a * a
    

def perimeter(a):
    '''Функция для вычисления периметра квадрата по длине стороны 
        Возвращает произведение введенной стороны а и 4
        При вводе 5, получаем 20'''
    return 4 * a
    
